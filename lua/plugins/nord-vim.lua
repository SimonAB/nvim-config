-- Configuration for nord-vim
-- Arctic-inspired colour scheme

-- Nord theme configuration (vim variables)
vim.g.nord_contrast = true -- Make sidebars and popup menus like nvim-tree and telescope have a different background
vim.g.nord_borders = false -- Enable the border between verticaly split windows
vim.g.nord_disable_background = false -- Disable the setting of background color so that NeoVim can use your terminal background
vim.g.nord_italic = false -- Disables the italic effect for comments
vim.g.nord_uniform_diff_background = true -- Enables the uniform background color for diff view
vim.g.nord_bold = false -- Disables the bold effect
